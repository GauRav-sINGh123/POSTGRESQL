#Math Module in Python

import math



x=9.9
y=16

print(math.floor(x))  # Round downs to Nearest smaller number
print(math.ceil(x))   # Round ups to Nearest larger number
print(math.pi)        #Returns the value of pi
print(math.e)         #Returns the value of e
print(int(math.sqrt(y)))   #Returns the square root
