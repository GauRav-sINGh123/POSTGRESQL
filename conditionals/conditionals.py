choice = int(input("What's your favorite activity? \n1: Sleeping \n2: Running \n3: Swimming  "))

if choice == 1:
    print("You're like a cat: lazy but adorable!")
elif choice == 2:
    print("You're like a cheetah: fast and focused!")
elif choice == 3:
    print("You're like a dolphin: playful and smart!")
else:
    print("You're unique, like a mythical dragon!")
